package com.xiaoshu.entity;

public class Tj {

	
	private Integer num;
	private String bame;
	public Integer getNum() {
		return num;
	}
	public void setNum(Integer num) {
		this.num = num;
	}
	public String getBame() {
		return bame;
	}
	public void setBame(String bame) {
		this.bame = bame;
	}
	@Override
	public String toString() {
		return "Tj [num=" + num + ", bame=" + bame + "]";
	}
	
}
